    public static Color foliageColor(int x, int z, int y, MCAFile mcaFile) {
        int rsum = 0;
        int gsum = 0;
        int bsum = 0;
        int i=0;

        for (int rx = x-8; rx < x + 8; ++rx) {
            for (int rz = z - 8; rz < z + 8; ++rz) {
                if (rx < 0 || rx >= 32*16 || rz < 0 || rz >= 32*16)
                    continue;

                int biome = mcaFile.getBiomeAt(rx, y, rz);
                if (biome == -1)
                    continue;
                int color = Biomes.computeBiomeColor(biome, Math.max(0, y - 64), false);
                rsum += (color >> 16) & 0xFF;
                gsum += (color >> 8) & 0xFF;
                bsum += color & 0xFF;
                i++;
            }
        }

        int r = rsum / i;
        int g = gsum / i;
        int b = bsum / i;

        Color foliageColor = new Color(r, g, b, 255);
        foliageColor.multiply(FOLIAGE_BASE);
        return foliageColor;
    }

    public static final int GRASS_BASE = 147;
    public static final int FOLIAGE_BASE = 130;

